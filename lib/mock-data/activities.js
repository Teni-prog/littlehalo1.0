// Helper functions
export function getActivitiesByCategory(categoryId) {
  if (categoryId === 'all') return mockActivities;
  return mockActivities.filter(activity => activity.category === categoryId);
}

export function getActivitiesByAge(age) {
  return mockActivities.filter(activity => age >= activity.ageMin && age <= activity.ageMax);
}

export function searchActivities(query) {
  const lowerQuery = query.toLowerCase();
  return mockActivities.filter(activity => 
    activity.title.toLowerCase().includes(lowerQuery) ||
    activity.description.toLowerCase().includes(lowerQuery) ||
    activity.learningGoals.some(goal => goal.toLowerCase().includes(lowerQuery))
  );
}
